    private void btnCal2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCal2ActionPerformed
          x = Integer.parseInt(txtField1.getText());
          y = Integer.parseInt(txtField2.getText());
            
            if (x > y) {
                int temp;
                temp = x; //now x is empty
                x = y; // now y is empty
                y = temp; // all good
            } // logic swap lower value to higher value.for make normal case
            int sum = 0;
            
            for (int i = x; i<= y;i++) {
                sum += i;
                txtOutput.setText(sum + "");
            }
    }//GEN-LAST:event_btnCal2ActionPerformed

