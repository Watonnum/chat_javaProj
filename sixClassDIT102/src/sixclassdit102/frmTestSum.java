    private void btnCal4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCal4ActionPerformed
        x = Integer.parseInt(txtField1.getText());
        y = Integer.parseInt(txtField2.getText());
        String ans = "";
        
        if (x > y) {
                int temp;
                temp = x; //now x is empty
                x = y; // now y is empty
                y = temp; // all good
            }
          CSum1 seeSum = new CSum1();
          CSum1 seeSum1 = new CSum1(x);
          CSum1 seeSum2 = new CSum1(y);
          ans += seeSum.showOutput() + "\n";
          ans += seeSum1.showOutput() + "\n";
          ans += seeSum2.showOutput() + "\n";
          txtOutput.setText(ans +"");
    }//GEN-LAST:event_btnCal4ActionPerformed

